async function writeData() {
  try {
    // Assuming quantity is obtained from the input field with id "quantity"
    const quantity = document.getElementById("quantity").value;
    const id = document.getElementById("id").value;
    const to = document.getElementById("to").value;

	const ids = id.split(',').map(Number);
	const amounts = quantity.split(',').map(Number);

    const { write, hash } = await writeContract({
      address: contractAddress,
      abi: Abi,
      functionName: "publicMint",
      args: [to, ids , amounts],
      chainId: 11155111,
    });

	const transactionReceipt = await waitForTransactionReceipt({
		hash: hash,
	  });
	  
	console.log('Transaction Receipt:', transactionReceipt);
    console.log("Transaction successful:", write);
  } catch (err) {
    const trimmedError = err.message.split("\n").slice(0, 2);// Get the first line of the error message
    console.log(trimmedError);
    alert(trimmedError.join("\n"));
  }
}

